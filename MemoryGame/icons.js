function getIconsForGrid(theme, gridSize) {
    const pairCount = (gridSize * gridSize) / 2;
    const themeData = ICON_THEMES[theme];
    if (!themeData) return [];
    const available = themeData.icons.slice(0, pairCount);
    while (available.length < pairCount) {
        available.push(themeData.icons[available.length % themeData.icons.length]);
    }
    return available;
}
